package yte.summerp.oopjava.inheritance;

public interface Human {
	/**
	 *
	 * @return Normal work of human being within one week in minutes.
	 */
	int work();

	/**
	 *
	 * @return Overtime work of human being within one week in minutes.
	 */
	int workAgain();

	/**
	 *
	 * @return Over and over overtime work of human being within one week in crazy minutes.
	 */
	int workAgainAndAgain();
}
